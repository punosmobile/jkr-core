CREATE OR REPLACE FUNCTION JKR.KOHTEET_PAATTYNEILLA_RAKENNUKSILLA () RETURNS TABLE (KOHDE_ID INTEGER, PRT TEXT) AS $$
SELECT
	K.ID,
	R.PRT
FROM
	JKR.RAKENNUS R
	LEFT JOIN JKR.KOHTEEN_RAKENNUKSET KR ON KR.RAKENNUS_ID = R.ID
	LEFT JOIN JKR.KOHDE K ON KR.KOHDE_ID = K.ID
WHERE
	R.KAYTOSTAPOISTO_PVM IS NULL
	AND K.LOPPUPVM IS NOT NULL
ORDER BY
	K.ID;
$$ LANGUAGE SQL STABLE;

