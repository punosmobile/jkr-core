CREATE OR REPLACE FUNCTION JKR.Asuinkiinteisto_kohteet_vaarilla_ominaisuuksilla () RETURNS TABLE (KOHDE_ID INTEGER, PRT TEXT) AS $$
SELECT
	K.ID,
	R.PRT
FROM
	JKR.KOHDE K
	LEFT JOIN JKR.KOHTEEN_RAKENNUKSET KR ON KR.KOHDE_ID = K.ID
	LEFT JOIN JKR.RAKENNUS R ON R.ID = KR.RAKENNUS_ID
WHERE
	KOHDETYYPPI_ID = 7
	AND NOT (
		R.HUONEISTOMAARA > 0
		OR (
			R.RAKENNUKSENKAYTTOTARKOITUS_KOODI::INTEGER >= 010
			AND R.RAKENNUKSENKAYTTOTARKOITUS_KOODI::INTEGER <= 041
		)
		OR (
			R.RAKENNUSLUOKKA_2018::INTEGER >= 0110
			AND R.RAKENNUSLUOKKA_2018::INTEGER <= 0211
		)
		OR R.RAKENNUKSENOLOTILA_KOODI::INTEGER = 1
	);
$$ LANGUAGE SQL STABLE;

